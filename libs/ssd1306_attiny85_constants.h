#define SSD1306_SEGREMAPINV							0xA1
#define SSD1306_CHARGEPUMP							0x8D
#define SSD1306_SWITCHCAPVCC						0x02
#define SSD1306_NOP									0xE3

#define SSD1306_PAGESIZE 							8
#define SSD1306_WIDTH								128
#define SSD1306_HEIGHT								64
#define SSD1306_PAGE_BUFFER_SIZE					SSD1306_WIDTH
#define SSD1306_INTERNAL_WIDTH						128 / 4
#define SSD1306_INTERNAL_HEIGHT						64 / 4
#define SSD1306_INTERNAL_BUFFER_SIZE				(SSD1306_INTERNAL_WIDTH * SSD1306_INTERNAL_HEIGHT) / SSD1306_PAGESIZE

